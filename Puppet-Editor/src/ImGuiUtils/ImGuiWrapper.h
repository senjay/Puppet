	static Ref<Texture2D> PropertyImage(Ref<Texture2D>tex)
	{
		ImGui::Image(tex ? (void*)tex->GetRendererID() : (void*)TextureLibrary::GetInstance().GetWhiteTexture()->GetRendererID(), ImVec2(64, 64), ImVec2{ 0, 1 }, ImVec2{ 1, 0 });
		if (ImGui::IsItemHovered())
		{
			if (tex)
			{
				ImGui::BeginTooltip();
				ImGui::PushTextWrapPos(ImGui::GetFontSize() * 35.0f);
				ImGui::TextUnformatted(tex->GetPath().c_str());
				ImGui::PopTextWrapPos();
				ImGui::Image((void*)tex->GetRendererID(), ImVec2(384, 384));
				ImGui::EndTooltip();
			}
			if (ImGui::IsItemClicked())
			{
				std::filesystem::path texturePath = FileDialogs::OpenFile("Pic (*.jpg *.png *.tga)\0");
				if (!texturePath.empty())
					return TextureLibrary::GetInstance().FindorAdd(texturePath.stem().string(), texturePath.string());
			}
		}
		if (ImGui::BeginDragDropTarget())
		{
			if (const ImGuiPayload* payload = ImGui::AcceptDragDropPayload("CONTENT_BROWSER_ITEM"))
			{
				const wchar_t* path = (const wchar_t*)payload->Data;
				std::filesystem::path texturePath = std::filesystem::path("./assets") / path;
				Ref<Texture2D> texture = TextureLibrary::GetInstance().FindorAdd(texturePath.stem().string(), texturePath.string());
				if (texture->IsLoaded())
					return texture;
				else
					PP_WARN("Could not load texture {0}", texturePath.filename().string());
			}
			ImGui::EndDragDropTarget();
		}
		return nullptr;
	}
